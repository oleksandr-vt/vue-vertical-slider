<script>
import { Hooper, Slide } from 'hooper';
import 'hooper/dist/hooper.css';

export default {
  name: 'App',
  components: {
    Hooper,
    Slide,
  },
  data() {
    return {
      slides: [
        { content: 'Slide 1' },
        { content: 'Slide 2' },
        { content: 'Slide 3' },
        { content: 'Slide 4' },
        { content: 'Slide 5' },
      ],
      sliderSettings: {
        itemsToShow: 1,
        vertical: true,
        mouseDrag: false,
        touchDrag: false,
        wheelControl: false,
        keysControl: false,
        shortDrag: false,
      },
      sliderHeight: 500,
    }
  },
  mounted() {
    window.addEventListener('scroll', this.handleScroll, { passive: false })
    this.setPageHeight()
  },
  beforeDestroy() {
    window.removeEventListener('scroll', this.handleScroll, { passive: false })
  },
  methods: {
    destructureScrollOffset(currentScrollPosition) {
      const pageWrapper = this.$refs.pageWrapper
      const sliderWrapper = this.$refs.sliderWrapper
      const slidesPlaceholderPX = this.sliderHeight * this.slidesCount

      const { top: sliderTop, height: sliderHeight } = sliderWrapper.getBoundingClientRect()
      const { top: pageTop } = pageWrapper.getBoundingClientRect()
      const screenHeight = window.innerHeight

      const threshold = sliderTop - pageTop - screenHeight / 2 + sliderHeight / 2;

      if (currentScrollPosition < threshold) {
        return {
          pageOffset: currentScrollPosition,
          slideOffset: 0
        }
      }

      if (currentScrollPosition < threshold + slidesPlaceholderPX) {
        return {
          pageOffset: threshold,
          slideOffset: currentScrollPosition - threshold,
        }
      }

      return {
        pageOffset: currentScrollPosition - slidesPlaceholderPX,
        slideOffset: slidesPlaceholderPX,
      }
    },
    handleScroll() {
      const { slider, pageWrapper } = this.$refs
      const sliderTrack = slider.$el.querySelector('.hooper-track')
      const currentScrollPosition = document.documentElement.scrollTop

      const {
        pageOffset,
        slideOffset,
      } = this.destructureScrollOffset(currentScrollPosition)

      pageWrapper.style.transform = `translate3d(0px, ${-pageOffset}px, 0px)`
      sliderTrack.style.transform = `translate3d(0px, ${-slideOffset}px, 0px)`
    },
    setPageHeight() {
      const { pageWrapper, mainWrapper } = this.$refs
      const pageWrapperHeight = pageWrapper?.getBoundingClientRect()?.height || 0
      const slidesHeight = this.sliderHeight * this.slidesCount
      const totalHeight = pageWrapperHeight + slidesHeight;

      mainWrapper.style.minHeight = `${totalHeight}px`
    }
  },
  computed: {
    slidesCount() {
      return this.slides.length - 1
    },
  }
}
</script>

